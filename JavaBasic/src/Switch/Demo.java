package Switch;

public class Demo {

	public static void main(String[] args) {
		int number=5;
		
switch(number){
	
	case 5: System.out.println("no is 20");
	break;
	
	case 6: System.out.println("no is 30"); 
	break;
	
	case 7: System.out.println("no is 40");
	break;
	
	default : System.out.println("no is not 10, 30 , 40 ");
}
	}

}
